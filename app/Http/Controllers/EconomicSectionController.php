    {
        $section=EconomicSection::create($request->all());
        if($section){
            /**
             * user log
             */
            activity()->useLog(Auth::user()->name)
                           ->causedBy(Auth::user()->id)
                           ->performedOn($section)
                           ->withProperties($section)
                           ->log('Create Section for workType');
        }
        return view('worktype.economicsection');
    }

